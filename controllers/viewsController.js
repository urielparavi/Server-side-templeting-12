exports.updateUserData = catchAsync(async (req, res, next) => {
  // For this work we needed to use middleware urlencoded parser to get the data from
  // the form
  // console.log('UPDATING USER', req.body);

  // update/findByIdAndUpdate => If we needed to update a password we can't use these methods
  // because that's not going to run ths save middleware which will encrypt our password for database
  // usualy when we want to update the password in web apps we have a separate form and route for that
  // like we did in our API
  const updatedUser = await User.findByIdAndUpdate(
    req.user.id,
    {
      name: req.body.name,
      email: req.body.email,
    },
    {
      new: true,
      runValidators: true,
    },
  );

  res.status(200).render('account', {
    title: 'Your account',
    user: updatedUser,
  });
});
